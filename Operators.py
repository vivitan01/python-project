#Arithmetic operators
num1 =10
num2 =5

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) #this is modulus and it returns the remainder after division

#Comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2) #Equal to
print(num1 != num2) # Not equal to

#Assignment operators
x =20

print(x)

x *= 2
print(x)

#Logical Operators -and, or, not
print(30 < 45 and 100 > 250)
print(30 < 45 or 100 > 250)
print(not(30 < 45 or 100 > 250))
